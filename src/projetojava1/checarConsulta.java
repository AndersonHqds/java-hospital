    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtData.getText().equals("NR")){
            JOptionPane.showMessageDialog(this,"Desculpe ainda não temos uma data prevista, tente novamente mais tarde");
            jButton2.setEnabled(false);
        }
        else{
        jButton2.setEnabled(true);
        dado.confirmação(txtRg.getText());
        JOptionPane.showMessageDialog(this,"Consulta Confirmada");
        Principal menu = new Principal();
        menu.setVisible(true);
        dispose();}
    }//GEN-LAST:event_jButton2ActionPerformed

