    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(txtNome.getText().equals("")||txtRg.getText().equals("")||txtCpf.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Por favor preecher todos os campos");
        }
        else{
          
         String[] dados = dado.checarConsulta(txtRg.getText());
         txtData.setText(dados[0]);
         txtDoutor.setText(dados[1]);
         txtHorario.setText(dados[2]);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

